var LowestPrices = Project_Base.extend({
	  ///////////////////////////////////////////////////////////////////////////////////////
	  ///////////////////////////////////////////////////////////////////////////////////////
	  //private var
	  ///////////////////////////////////////////////////////////////////////////////////////
	  ///////////////////////////////////////////////////////////////////////////////////////
	  //---------------------------------------------------------
	  //$elm
	  //---------------------------------------------------------
	  $items:undefined,
	  $gen_txtBox:undefined,
	  $cta:undefined,
	  $cta_text:undefined,
	  //---------------------------------------------------------
	  //Boolean
	  //---------------------------------------------------------
	  callStart:true,
	  //---------------------------------------------------------
	  //Object
	  //---------------------------------------------------------
	  info:undefined,
	  //---------------------------------------------------------
	  //String
	  //---------------------------------------------------------
	  traceID:undefined,
	  size:undefined, 
	  ///////////////////////////////////////////////////////////////////////////////////////
	  ///////////////////////////////////////////////////////////////////////////////////////
	  //Constructor
	  ///////////////////////////////////////////////////////////////////////////////////////
	  ///////////////////////////////////////////////////////////////////////////////////////
	  init:function(obj, init_preStart) {
		  this._super(obj, false);
	  },
	  //-------------------------------------------------------------------------------------
	  mouseover:function(e) {
		  //----------------------------------------------------
		  //init
		  //----------------------------------------------------
		  var delay = 0;
		  var $elm;
		  //----------------------------------------------------
		  this.mouseover = true;
		  if (!this.active) {
			  this.active = true;
			  //----------------------------------------------------
			  $elm = this.$cta_txt;
			  TweenEngine.to($elm, .2, {y:"+50", ease:Expo.easeIn});
			  //----------------------------------------------------
			  delay += .2;
			  TweenEngine.delayedCall(delay, $.proxy(this.mouseover_complete, this));
		  }
	  },
	  //-------------------------------------------------------------------------------------
	  mouseover_complete:function(e) {
		  //----------------------------------------------------
		  //init
		  //----------------------------------------------------
		  var $elm;
		  var delay = 0;
		  //----------------------------------------------------
		  $elm = this.$cta_txt;
		  TweenEngine.set($elm, {y:-50});
		  TweenEngine.to($elm, .2, {y:0, ease:Back.easeOut, onComplete:$.proxy(function() {this.active = false;}, this)});
	  },
	  //------------------------------------------------------------------------------------- 
	  div_CFG:function() {
		  this._super();
		  this.$items = this.$div.children(".items");
		  this.$gen_txtBox = this.$div.find(".gen_txtBox_inner");
		   this.$cta = this.$div.find(".cta");
		  this.$cta_txt = this.$div.find(".cta_txt");
		  this.$div.mouseenter($.proxy(this.mouseover, this));
		  //this.$div.mouseout($.proxy(this.mouseout, this));
	  },
	  //------------------------------------------------------------------------------------- 
	  visibility_CFG:function() {
		  TweenEngine.set(this.$div.find(".bg"), {opacity:1});
		  TweenEngine.set(this.$items, {opacity:1});
		  TweenEngine.set(this.$items.children(), {opacity:0});
		  TweenEngine.set(this.$gen_txtBox.parent(), {opacity:1});
		  TweenEngine.set(this.$gen_txtBox.children(), {opacity:0});
	  }
});
